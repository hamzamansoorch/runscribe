"""Render a :class:`~runscribe.build.builder.Runbook` to a standalone HTML page.

Dependency-free and deterministic: no Markdown library, no network, all CSS
inlined so the file works when opened directly or shared as a single attachment.
Everything user-derived is HTML-escaped.
"""

from __future__ import annotations

from html import escape

from ..session import StepKind
from .builder import Runbook, RunbookStep

_CSS = """\
:root { color-scheme: light dark; }
body { font: 16px/1.6 system-ui, sans-serif; max-width: 46rem; margin: 2rem auto;
       padding: 0 1rem; }
h1 { margin-bottom: .2rem; }
.meta { color: #888; font-size: .85rem; margin-top: 0; }
h2 { margin-top: 2rem; border-bottom: 1px solid #8883; padding-bottom: .2rem; }
pre { background: #8881; border-radius: 6px; padding: .7rem 1rem; overflow-x: auto; }
code { font: .9rem/1.5 ui-monospace, monospace; }
.note { margin: .8rem 0; }
.warn { color: #b45; font-size: .85rem; margin: .3rem 0 0; }
.redacted { color: #888; font-size: .85rem; margin: .3rem 0 0; }
"""


def render_html(runbook: Runbook) -> str:
    parts: list[str] = [
        "<!doctype html>",
        '<html lang="en"><head><meta charset="utf-8">',
        '<meta name="viewport" content="width=device-width, initial-scale=1">',
        f"<title>{escape(runbook.title)}</title>",
        f"<style>{_CSS}</style>",
        "</head><body>",
        f"<h1>{escape(runbook.title)}</h1>",
        f'<p class="meta">Generated by runscribe · {escape(runbook.created)}</p>',
    ]

    for step in runbook.steps:
        if step.kind is StepKind.SECTION:
            parts.append(f"<h2>{escape(step.text.strip())}</h2>")
        elif step.kind is StepKind.NOTE:
            parts.append(f'<p class="note">{escape(step.text.strip())}</p>')
        elif step.kind is StepKind.COMMAND:
            parts.append(_render_command(step))

    parts.append("</body></html>")
    return "\n".join(parts) + "\n"


def _render_command(step: RunbookStep) -> str:
    block = [f"<pre><code>{escape(step.text)}</code></pre>"]
    if step.exit_code not in (None, 0):
        block.append(
            f'<p class="warn">⚠ exited with code {step.exit_code} when recorded.</p>'
        )
    if step.redacted:
        block.append('<p class="redacted">🔒 a secret was redacted from this step.</p>')
    return "\n".join(block)
